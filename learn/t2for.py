# 迭代器
for i in "hello":
    print(i)


# 0-n not included n
for i in range(5):
    print(i)
    print('hello')

print()
# n-m not included m
for i in range(3, 5):
    print(i)
    print('hello')

print()
# n-m-step
for i in range(1, 10, 3):
    print(i)
    print('hello')

print()
# n-m-step
for i in range(10, 1, -3):
    print(i)
    print('hello')
